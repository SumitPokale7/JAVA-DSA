package BeginnerQuestions;

// Q15. Volume Of Prism.

public class Volume_Of_Prism_15 {
    public static void main(String[] args) {

        Prism(5, 10);
    }

    static void Prism(int Base, int Height) {
        int Prism = Base * Height;

        System.out.println("Volume Of Prism: " + Prism);
    }
}
